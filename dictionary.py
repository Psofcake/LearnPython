d2={'이름':["철수","영희","맹구"],'나이':[10,20,30],'참석유무':[True,False,True]}
print(d2['이름'][0])

#dictionary 메서드
print("딕셔너리 메서드")
print(d1.items()) #(키,값)쌍을 리스트 형태로 반환
print(d1.keys()) #키들을 리스트로 반환
print(d1.values()) #값들을 리스트로 반환
