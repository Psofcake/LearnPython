print("hello") #문자열
print(1) #숫자
print("hi",1) #여러 타입 함께 입력 시 , 사용
print("안녕\t반가워") #\t : 탭
print("안녕\n반가워") #\n : 줄바꿈

name = "경기" #변수 선언
age = 50
print(name) #변수 출력 방법
print(f"내이름은 {name}야") #f스트링 : 문자열 내 {변수명}사용
print("내이름은 {}이야 그리고 나이는 {}이야".format(name,age)) #.format(변수명)사용. 변수를 순차적으로 입력.

#변수명 규칙 : my_name(스네이크표기법, 주로 변수명) 또는 MyName(파스칼표기법, 주로 클래스), 특수문자는 _만, 숫자로 변수명을 시작할 수 없음.
